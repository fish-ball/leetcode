class Solution:
    def hammingWeight(self, n: int) -> int:
        n = (n & 0b01010101010101010101010101010101) + ((n>>1) & 0b01010101010101010101010101010101)
        # print(f'{n:032b}')
        n = (n & 0b00110011001100110011001100110011) + ((n>>2) & 0b00110011001100110011001100110011)
        # print(f'{n:032b}')
        n = (n & 0b00001111000011110000111100001111) + ((n>>4) & 0b00001111000011110000111100001111)
        # print(f'{n:032b}')
        n = (n & 0b00000000111111110000000011111111) + ((n>>8) & 0b00000000111111110000000011111111)
        # print(f'{n:032b}')
        return (n&((1<<16)-1)) + (n>>16)
